/* ---- Helpers ---- */

let currentLang = "en";

/** Load language from localStorage on page load, default to "en" */
function loadLanguage() {
  const saved = localStorage.getItem("equal_lang");
  if (saved && I18N[saved]) {
    currentLang = saved;
  }
  applyLanguage(currentLang);
  updateLangButtons(currentLang);
}

/** Set a new language and persist it */
function setLanguage(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem("equal_lang", lang);
  applyLanguage(lang);
  updateLangButtons(lang);
}

/** Translate the entire page DOM */
function applyLanguage(lang) {
  const dict = I18N[lang];
  if (!dict) return;

  // Update HTML lang attribute
  document.documentElement.lang = lang;

  // All elements with data-i18n
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  // Placeholders (data-i18n-placeholder)
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) el.placeholder = dict[key];
  });
}

/** Update the lang-switcher button states */
function updateLangButtons(lang) {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });
}

/** Get a translation string for JS use (toasts etc.) */
function t(key) {
  return I18N[currentLang]?.[key] ?? I18N.en[key] ?? key;
}
